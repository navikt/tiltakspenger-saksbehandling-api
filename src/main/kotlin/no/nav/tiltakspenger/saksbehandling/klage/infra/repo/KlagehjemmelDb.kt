        }

        fun List<Klagehjemmel>.toDb(): List<KlagehjemmelDb> = this.map { it.toDb() }

        fun List<KlagehjemmelDb>.toDomain(): List<Klagehjemmel> = this.map { it.toDomain() }
    }
}
